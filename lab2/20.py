class Solution:
    def isValid(self, s):
        stack = []  # 用栈来存左括号 / Используем стек для хранения открывающих скобок
        mp = {')': '(', ']': '[', '}': '{'}  # 右括号和对应左括号的配对关系 / Соответствие закрывающих и открывающих скобок

        for c in s:  # 遍历字符串中的每个字符 / Проходим по каждому символу строки
            if c in mp:  # 如果当前字符是右括号 / Если текущий символ — закрывающая скобка
                if not stack or stack.pop() != mp[c]:
                    return False  # 栈空或者不匹配，返回 False / Если стек пуст или скобки не совпадают, возвращаем False
            else:
                stack.append(c)  # 如果当前字符是左括号，就入栈 / Если текущий символ — открывающая скобка, добавляем в стек

        return not stack  # 栈空说明匹配成功 / Пустой стек означает, что все скобки корректно закрыты