def maxArea(height):
    # 方法：左右双指针
    # Метод: два указателя (left / right)
    # 时间复杂度 / Сложность: O(n)

    left = 0
    right = len(height) - 1
    max_area = 0

    while left < right:
        # 当前宽度
        # Текущая ширина
        width = right - left

        # 当前高度由较短的一边决定
        # Текущая высота определяется более низкой линией
        current_height = min(height[left], height[right])

        # 当前面积
        # Текущая площадь
        area = width * current_height

        # 更新最大面积
        # Обновляем максимальную площадь
        if area > max_area:
            max_area = area

        # 移动较短的一边
        # Сдвигаем указатель у более низкой линии
        if height[left] < height[right]:
            left += 1
        else:
            right -= 1

    return max_area


print(maxArea([1, 8, 6, 2, 5, 4, 8, 3, 7]))
print(maxArea([1, 1]))


#Я использовал метод двух указателей: left и right.
#Левый указатель стоит в начале массива, правый — в конце.
#На каждом шаге я вычисляю площадь контейнера:
#это ширина умножить на меньшую из двух высот.
#После этого обновляю максимальную площадь.
#Дальше сдвигаю указатель у более низкой линии, потому что высота контейнера ограничивается более низкой стороной.
#Только так можно попытаться получить большую площадь.
#Сложность алгоритма — O(n).


#我用了左右双指针。
#左指针从数组左边开始，右指针从右边开始。
#每一步先计算当前两条线组成的容器面积，面积等于宽度乘以两条线中较小的高度。
#然后更新最大面积。
#接下来移动较短的那一边，因为容器高度由短板决定，只有移动较短的一边，才有可能找到更大的面积。
#时间复杂度是 O(n)。