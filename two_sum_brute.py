def twoSum(nums, target):
    # 方法：暴力法
    # Метод: brute force
    # 时间复杂度 / Сложность: O(n^2)

    # 外层循环选择第一个元素
    # Внешний цикл выбирает первый элемент
    for i in range(len(nums)):
        # 内层循环选择第二个元素
        # 从 i + 1 开始，避免重复和自己配对
        # Внутренний цикл выбирает второй элемент
        # Начинаем с i + 1, чтобы не брать тот же элемент и не проверять пары повторно
        for j in range(i + 1, len(nums)):
            # 如果两个元素的和等于 target，返回它们的下标
            # Если сумма двух элементов равна target, возвращаем их индексы
            if nums[i] + nums[j] == target:
                return [i, j]

    # 如果没有找到答案，返回空列表
    # Если решение не найдено, возвращаем пустой список
    return []


print(twoSum([2, 7, 11, 15], 9))
print(twoSum([3, 2, 4], 6))
print(twoSum([3, 3], 6))




