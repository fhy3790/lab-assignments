def twoSum(nums, target):
    # 方法：哈希表（字典）
    # Метод: хеш-таблица (словарь)
    # 时间复杂度 / Сложность: O(n)

    # seen 中保存：数字 -> 下标
    # В seen храним: число -> индекс
    seen = {}

    # 遍历数组
    # Проходим по массиву
    for i in range(len(nums)):
        # 当前元素
        # Текущий элемент
        current = nums[i]

        # 需要找到的补数
        # Число, которое нужно найти
        need = target - current

        # 如果补数已经出现过，就返回答案
        # Если нужное число уже встречалось, возвращаем ответ
        if need in seen:
            return [seen[need], i]

        # 否则把当前元素存入字典
        # Иначе сохраняем текущий элемент в словарь
        seen[current] = i

    # 如果没有找到答案，返回空列表
    # Если решение не найдено, возвращаем пустой список
    return []


print(twoSum([2, 7, 11, 15], 9))
print(twoSum([3, 2, 4], 6))
print(twoSum([3, 3], 6))


#Я использовал словарь, где сохраняются уже встреченные числа и их индексы.
#Для текущего элемента вычисляю need = target - current.
#Если это число уже есть в словаре, возвращаю ответ.
#Иначе сохраняю текущий элемент в словарь.
#Сложность алгоритма — O(n).

