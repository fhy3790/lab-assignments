def isPalindrome(s):
    # 方法：左右双指针
    # Метод: два указателя (left / right)
    # 时间复杂度 / Сложность: O(n)

    left = 0
    right = len(s) - 1

    while left < right:
        # 跳过左边不是字母或数字的字符
        # Пропускаем слева символы, которые не являются буквами или цифрами
        while left < right and not s[left].isalnum():
            left += 1

        # 跳过右边不是字母或数字的字符
        # Пропускаем справа символы, которые не являются буквами или цифрами
        while left < right and not s[right].isalnum():
            right -= 1

        # 比较左右字符，忽略大小写
        # Сравниваем левый и правый символы без учёта регистра
        if s[left].lower() != s[right].lower():
            return False

        # 两个指针继续向中间移动
        # Сдвигаем оба указателя к центру
        left += 1
        right -= 1

    return True


print(isPalindrome("A man, a plan, a canal: Panama"))
print(isPalindrome("race a car"))
print(isPalindrome(" "))


#Я использовал метод двух указателей: left и right.
#Левый указатель идёт слева направо, правый — справа налево.
#Если символ не является буквой или цифрой, я его пропускаю.
#Если символы допустимые, сравниваю их в нижнем регистре.
#Если они не равны, строка не является палиндромом.
#Если все символы совпали, возвращаю True.
#Сложность алгоритма — O(n).


#我用了左右双指针。
#左指针从字符串左边开始，右指针从右边开始。
#如果当前字符不是字母或数字，就跳过。
#如果是有效字符，就把它们转成小写后比较。
#如果不同，说明不是回文，返回 False。
#如果一直都相同，最后返回 True。
#时间复杂度是 O(n)。

